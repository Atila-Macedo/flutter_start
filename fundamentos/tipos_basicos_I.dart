/*
  Números - int e double
  String - (String)
  Booleano - (bool)
*/

void main()
{
  int numero = 10; // int
  double decimal1 = -10.5.abs(); // double
  double decimal2 = double.parse("12.765"); // double
  print( 'Número: $numero'); // Exibe o número
  print( 'Decimal1: $decimal1'); // Exibe o decimal
  print( 'Decimal2: $decimal2'); // Exibe o decimal2
}