// 5. Utilize dynamic para criar uma variável que começa com valor 10 , depois recebe um texto e depois um true . Imprima após cada alteração.

void main() {
  dynamic variavel = 10; // Inicializa com um número inteiro
  print(variavel); // Imprime: 10

  variavel = "Texto"; // Atribui uma string
  print(variavel); // Imprime: Texto

  variavel = true; // Atribui um booleano
  print(variavel); // Imprime: true
}