// 3. Declare três variáveis: uma bool , uma double e uma int . Atribua valores e imprima todas.

void main() {
  bool isTrue = true; // Declaração da variável booleana
  double decimal = 3.14; // Declaração da variável decimal
  int numero = 42; // Declaração da variável inteira

  print("Booleano: $isTrue"); // Impressão do valor booleano
  print("Decimal: $decimal"); // Impressão do valor decimal
  print("Número: $numero"); // Impressão do valor inteiro

  // Ou podemos imprimir todos os valores em uma única linha
  print("Booleano: $isTrue, Decimal: $decimal, Número: $numero"); // Impressão de todos os valores em uma linha
}
